<script>
    function login(){
    data =$("form").serialize();
    $.ajax({
        url:'verify_login',
        type:'post',
        data:data,
        cache: false,
            success:function(res)
            {
                if(res.error){
                    toastr.error(res.error);
                    $('#Login').prop('disabled', false);
                }
                else {
                    toastr.success(res.success);
                    $('#Login').prop('disabled', false);
                    window.location.href="dashboard";

                }
            }
        });
    }
    </script>
